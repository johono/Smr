    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        ResultSet rs;
        if("".equals(this.codigo.getText())){
            rs = this.cidades.Ler_Cidade(cb.getSelectedItem().toString());
        }else{
            rs = this.cidades.Ler_Cidade(codigo.getText());
        }
        if(rs != null){
            try {
                rs.first();
                this.codigo.setText(rs.getString("COD_CIDADE"));
                this.nome.setText(rs.getString("NOME_CIDADE"));
            } catch (SQLException ex) {
                javax.swing.JOptionPane.showMessageDialog(null, "NENHUM REGISTRO ENCONTRADO.", "Informacao", 1);
            }
        }else{
            javax.swing.JOptionPane.showMessageDialog(null, "NENHUM REGISTRO ENCONTRADO.", "Informacao", 1);
        }
    }//GEN-LAST:event_btPesquisarActionPerformed

    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        String nome_cidade = this.nome.getText();
        if(!"".equals(nome_cidade)){
            ResultSet rs = this.cidades.Ler_Cidade(nome_cidade);            
            try {
                if(!rs.first()){
                    this.cidades.Gravar_Cidade(nome_cidade);
                    Atualiza_Cidade();
                }else{
                    javax.swing.JOptionPane.showMessageDialog(null, "CIDADE JÁ CADASTRADA.", "Informacao", 1);
                }
            } catch (SQLException ex) {
                javax.swing.JOptionPane.showMessageDialog(null, "NENHUM REGISTRO ENCONTRADO.", "Informacao", 1);
            }
        }else{
            javax.swing.JOptionPane.showMessageDialog(null, "INFORME O NOME DA CIDADE.", "Informacao", 1);
        }
    }//GEN-LAST:event_btSalvarActionPerformed

