    private void Atualiza_Cidade(){
        try {
            this.cb.removeAllItems();
            ResultSet rs = this.cidades.Ler_Cidade();
            if(rs.next()){
                rs.previous();
                while(rs.next()){
                    this.cb.addItem(rs.getString("nome_cidade"));
                }
            }
            this.cb.addItem("");
            
            this.codigo.setText("");
            this.codigo.setText("00000");
            this.nome.setText("");
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(Cidades.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

