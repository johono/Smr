    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        String cidade = this.nome.getText();
        if(!"".equals(cidade)){
           String cod = this.codigo.getText();
           this.cidades.Altera_Cidade(cod, cidade);
           Atualiza_Cidade();
        }else{
            javax.swing.JOptionPane.showMessageDialog(null, "O CAMPO NOME DEVE ESTAR PREENCHIDO.", "Informacao", 1);
        }
    }//GEN-LAST:event_btAlterarActionPerformed

